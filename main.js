function toggle(id) {
    a = document.getElementById('toggle_'+id);
    b = document.getElementById('display_'+id);
      if (a.style.display=='block') {
        a.style.display='none';
        b.innerHTML='Show information';
      }
      else {
        a.style.display='block';
        b.innerHTML='Hide information';
    }
  }
